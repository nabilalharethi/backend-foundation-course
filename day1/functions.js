//Function declaration
function add(a,b){
    return a + b;
}

// Arow function (modern sysntax)
const multiply = (a,b) => {
    return a * b;
};

//Even shorter arrow function
const subtract = (a,b) => a - b;

console.log(add(5,3)); //8
console.log(multiply(4,6)); //24
console.log(subtract(10,4)); //6
